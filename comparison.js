let m = 5, n = '5';

console.log(m == n);   // true (values are equal)
console.log(m === n);  // false (different types)
console.log(m != n);   // false
console.log(m !== n);  // true
console.log(m > 3);    // true
console.log(m < 10);   // true
console.log(m >= 5);   // true
console.log(m <= 4);   // false
